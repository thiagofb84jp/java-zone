package w3schools.exercises.variables;

public class Exercise04 {
    /**
     * 4. Write a Java program to print the results of the following operations.
     */
    public static void main(String[] args) {
        int operation1 = -5 + 8 * 6;
        double operation2 = (55 + 9) % 9;
        double operation3 = 20 + -3 * 5 / 8;
        double operation4 = 5 + 15 / 3 * 2 - 8 % 3;

        System.out.println(operation1);
        System.out.println(operation2);
        System.out.println(operation3);
        System.out.println(operation4);
    }
}
